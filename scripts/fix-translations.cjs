function flatten(obj, prefix = '') {
  const result = {};
  for (const key in obj) {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    if (typeof obj[key] === 'object' && obj[key] !== null && !Array.isArray(obj[key])) {
      Object.assign(result, flatten(obj[key], fullKey));
    } else {
      result[fullKey] = obj[key];
    }
  }
  return result;
}

const reloadEN = JSON.parse(fs.readFileSync(enPath, 'utf8'));
const reloadIT = JSON.parse(fs.readFileSync(itPath, 'utf8'));
const flatEN = flatten(reloadEN);
const flatIT = flatten(reloadIT);

let remaining = 0;
for (const key in flatIT) {
  const itVal = flatIT[key];
  const enVal = flatEN[key];
  if (typeof itVal === 'string' && typeof enVal === 'string' && itVal === enVal) {
    if (itVal.length > 3 && /^[A-Z]/.test(itVal) && !excludeTerms.has(itVal.trim())) {
      remaining++;
      console.log(`  Still untranslated: "${key}": "${itVal}"`);
    }
  }
}
console.log(`\nRemaining untranslated: ${remaining}`);
