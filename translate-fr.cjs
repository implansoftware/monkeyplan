// ============ TRANSLATION ENGINE ============

function translateValue(value, keyPath) {
  if (typeof value !== 'string') return value;
  if (value === '') return '';
  
  // Keep technical/special strings
  if (/^[A-Z0-9_.\-\/]+$/i.test(value) && value.length <= 10 && !/[a-z]{3,}/.test(value)) return value;
  if (/^https?:\/\//.test(value)) return value;
  if (/^[0-9.,€$%]+$/.test(value)) return value;
  if (/^\+?\d[\d\s\-().]*$/.test(value)) return value;
  if (/^\{\{.*\}\}$/.test(value.trim())) return value;
  if (/^[a-z0-9_]+$/.test(value) && value.length <= 30 && !value.includes(' ')) return value;
  
  // Exact dictionary lookup
  if (exact[value]) return exact[value];
  
  // Apply word-by-word translation
  return wordByWord(value);
}

function wordByWord(text) {
  if (!text) return text;
  
  // Preserve HTML tags and {{placeholders}}
  const parts = text.split(/(<[^>]+>|\{\{[^}]+\}\})/g);
  
  return parts.map(part => {
    if (/^<[^>]+>$/.test(part) || /^\{\{[^}]+\}\}$/.test(part)) return part;
    return translateWords(part);
  }).join('');
}

function translateWords(text) {
  if (!text) return text;
  
  // First try exact on entire text
  if (exact[text]) return exact[text];
  if (exact[text.trim()]) return exact[text.trim()];
  
  // Split preserving delimiters
  const tokens = text.split(/(\s+|[.,;:!?()\[\]{}'"\/\\])/g);
  
  return tokens.map(token => {
    if (!token || /^\s+$/.test(token) || /^[.,;:!?()\[\]{}'"\/\\]$/.test(token)) return token;
    
    const lower = token.toLowerCase();
    if (wordDict[lower]) {
      // Preserve capitalization
      if (token === token.toUpperCase() && token.length > 1) {
        return wordDict[lower].toUpperCase();
      }
      if (token[0] === token[0].toUpperCase() && token.length > 1) {
        const tr = wordDict[lower];
        return tr.charAt(0).toUpperCase() + tr.slice(1);
      }
      return wordDict[lower];
    }
    
    return token;
  }).join('');
}

function translateObject(obj, parentKey) {
  if (typeof obj === 'string') return translateValue(obj, parentKey);
  if (Array.isArray(obj)) return obj.map((item, i) => translateObject(item, parentKey + '[' + i + ']'));
  if (typeof obj === 'object' && obj !== null) {
    const result = {};
    for (const key of Object.keys(obj)) {
      result[key] = translateObject(obj[key], (parentKey ? parentKey + '.' : '') + key);
    }
    return result;
  }
  return obj;
}

